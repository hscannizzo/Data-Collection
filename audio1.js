const beach2Audio = document.getElementById('beach2audio')

beach2Frame.addEventListener('mouseenter', function() {
  // Add the line below if you want the audio to start from the beginning everytime you play
 beach2Audio.currentTime = 0
  beach2Audio.play()
})

beach2Frame.addEventListener('mouseleave', function() {
  beach2Audio.pause()
})



